package com.javarush.tasts.level4.lesson10.task1;

public class Solution {

    /**
     Вывести на экран числа от 1 до 10, используя цикл while.
     */

    public static void main(String[] args) {

        int i = 1;
        while (i <= 10){
            System.out.println(i++);
        }
    }
}
